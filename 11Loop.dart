/* Repetition of same line or lines of code again and again is called looping and the way to craete a that state is by using loops 
Types of loop:
1. For loop
2. While loop
3. Do While loop
4. for each loop
*/

void main() {
  // for loop - Three parts of for loop ??
  /* for(start_condtion, ending_condition, incremening_measures)  */

  // Increment 
  for (int i = 0; i < 10; i++) {
    print(i);
  }

  // Decrement 
  for (int i = 100; i >= 1; i--) {
    print(i);
  }
}
